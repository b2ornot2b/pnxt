/**
 * Helper: create a standard agent config with sensible defaults.
 */
export function makeAgentConfig(overrides: Partial<AgentConfig> & { id: string; name: string; type: AgentConfig['type'] }): AgentConfig {
  return {
    behavior: {
      style: 'balanced',
      verbosity: 'normal',
      uncertainty_response: 'ask',
      instructions: '',
    },
    capabilities: {
      tools: [],
      concurrency: 1,
      token_budget: 10000,
      time_budget: 30000,
    },
    memory: {
      preload_topics: [],
      working_memory_limit: 100,
      persist_sessions: false,
    },
    trust_level: 1,
    ...overrides,
  };
}

/**
 * Helper: create a tool registration with sensible defaults.
 */
export function makeToolRegistration(
  name: string,
  sideEffects: ToolRegistration['sideEffects'] = ['none'],
): ToolRegistration {
  return {
    name,
    description: `Tool: ${name}`,
    inputSchema: { type: 'object' },
    outputSchema: { type: 'object' },
    sideEffects,
    ops: {
      timeout: 10000,
      retryable: false,
      idempotent: true,
      costCategory: 'cheap',
    },
  };
}

/**
 * Helper: create an offered capability.
 */
export function makeOfferedCapability(
  operation: string,
  requiredTrustLevel: TrustLevel = 0,
  version: CapabilityVersion = { major: 1, minor: 0, patch: 0 },
): OfferedCapability {
  return {
    operation,
    version,
    description: `Capability: ${operation}`,
    requiredTrustLevel,
    defaultConstraints: {},
  };
}

/**
 * Scenario: Planning agent delegates to coding agent, review agent validates.
 *
 * Tests the delegation pattern where a higher-trust planning agent coordinates
 * work among specialized agents with varying trust levels.
 */
export function createDelegationScenario(): CoordinationScenario {
  const now = new Date().toISOString();

  return {
    name: 'delegation-pattern',
    description: 'Planning agent delegates coding tasks, review agent validates results',
    agents: [
      makeAgentConfig({ id: 'planner-1', name: 'Planner', type: 'planning', trust_level: 3 }),
      makeAgentConfig({ id: 'coder-1', name: 'Coder', type: 'coding', trust_level: 1 }),
      makeAgentConfig({ id: 'reviewer-1', name: 'Reviewer', type: 'review', trust_level: 2 }),
    ],
    tools: [
      { registration: makeToolRegistration('file.read', ['file_read']), handler: async () => ({ content: 'file contents' }) },
      { registration: makeToolRegistration('file.write', ['file_write']), handler: async () => ({ written: true }) },
      { registration: makeToolRegistration('git.commit', ['git']), handler: async () => ({ sha: 'abc123' }) },
      { registration: makeToolRegistration('test.run', ['process']), handler: async () => ({ passed: 5, failed: 0 }) },
    ],
    capabilities: [
      makeOfferedCapability('file.read', 0),
      makeOfferedCapability('file.write', 1),
      makeOfferedCapability('git.commit', 2),
      makeOfferedCapability('test.run', 3),
    ],
    tasks: [
      {
        agentId: 'planner-1',
        description: 'Plan feature implementation',
        toolsRequired: ['file.read'],
        outcome: 'success',
        trustEvents: [{ agentId: 'planner-1', reason: 'task_success', timestamp: now }],
        memoryEntries: [{
          type: 'episodic',
          content: 'Planned feature: add user authentication',
          metadata: { source: 'planner-1', confidence: 0.9, topics: ['planning', 'auth'], entities: ['planner-1'], timestamp: now },
        }],
      },
      {
        agentId: 'coder-1',
        description: 'Implement authentication module',
        toolsRequired: ['file.read', 'file.write'],
        outcome: 'success',
        trustEvents: [{ agentId: 'coder-1', reason: 'task_success', timestamp: now }],
        memoryEntries: [{
          type: 'episodic',
          content: 'Implemented auth module in src/auth.ts',
          metadata: { source: 'coder-1', confidence: 0.85, topics: ['coding', 'auth'], entities: ['coder-1'], timestamp: now },
        }],
      },
      {
        agentId: 'reviewer-1',
        description: 'Review authentication implementation',
        toolsRequired: ['file.read', 'git.commit'],
        outcome: 'revision',
        trustEvents: [
          { agentId: 'reviewer-1', reason: 'task_success', timestamp: now },
          { agentId: 'coder-1', reason: 'task_revision', timestamp: now },
        ],
        memoryEntries: [{
          type: 'semantic',
          content: 'Auth module needs input validation on email field',
          metadata: { source: 'reviewer-1', confidence: 0.95, topics: ['review', 'auth', 'security'], entities: ['reviewer-1', 'coder-1'], timestamp: now },
        }],
      },
    ],
  };
}

/**
 * Scenario: Trust escalation through demonstrated reliability.
 *
 * A new agent starts at trust level 0 and earns higher trust through
 * successful task completions, eventually gaining access to more tools.
 */
export function createTrustEscalationScenario(): CoordinationScenario {
  const now = new Date().toISOString();

  const successEvents = (agentId: string, count: number): TrustEvent[] =>
    Array.from({ length: count }, () => ({
      agentId,
      reason: 'task_success' as const,
      timestamp: now,
    }));

  return {
    name: 'trust-escalation',
    description: 'New agent earns trust through demonstrated reliability',
    agents: [
      makeAgentConfig({ id: 'newcomer-1', name: 'Newcomer', type: 'coding', trust_level: 0 }),
    ],
    tools: [
      { registration: makeToolRegistration('file.read', ['file_read']), handler: async () => ({ content: 'data' }) },
      { registration: makeToolRegistration('file.write', ['file_write']), handler: async () => ({ written: true }) },
    ],
    capabilities: [
      makeOfferedCapability('file.read', 0),
      makeOfferedCapability('file.write', 1),
    ],
    tasks: [
      // First task: agent at level 0 can only read
      {
        agentId: 'newcomer-1',
        description: 'Read and analyze codebase',
        toolsRequired: ['file.read', 'file.write'],
        outcome: 'success',
        trustEvents: successEvents('newcomer-1', 3),
        memoryEntries: [{
          type: 'episodic',
          content: 'Analyzed codebase structure',
          metadata: { source: 'newcomer-1', confidence: 0.8, topics: ['analysis'], entities: ['newcomer-1'], timestamp: now },
        }],
      },
      // More successes to build trust
      {
        agentId: 'newcomer-1',
        description: 'Complete additional analysis tasks',
        toolsRequired: ['file.read'],
        outcome: 'success',
        trustEvents: successEvents('newcomer-1', 3),
        memoryEntries: [{
          type: 'episodic',
          content: 'Completed code analysis with high accuracy',
          metadata: { source: 'newcomer-1', confidence: 0.9, topics: ['analysis'], entities: ['newcomer-1'], timestamp: now },
        }],
      },
    ],
  };
}

