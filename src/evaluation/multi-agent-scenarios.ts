/**
 * Multi-Agent Coordination Scenarios — empirical evaluation of ANP patterns.
 *
 * Defines reusable scenario types and a runner that exercises the full system:
 * AgentRuntime + TrustEngine + ACIGateway + CapabilityNegotiation + MemoryService.
 *
 * Each scenario simulates a realistic multi-agent development workflow and
 * collects observable outcomes for evaluation.
 */

import type { AgentConfig, TrustLevel } from '../types/agent.js';
import type { ToolRegistration } from '../types/aci.js';
import type { OfferedCapability, CapabilityVersion } from '../types/capability.js';
import type { TrustEvent } from '../types/trust.js';
import type { MemoryEntry } from '../types/memory.js';
import type { AgentRuntime } from '../agent/agent-runtime.js';
import type { TrustEngine } from '../trust/trust-engine.js';
import type { ACIGateway, ToolHandler } from '../aci/aci-gateway.js';
import type { CapabilityNegotiation } from '../capability/capability-negotiation.js';
import type { MemoryService } from '../memory/memory-service.js';

/**
 * A task assigned to an agent within a scenario.
 */
export interface ScenarioTask {
  agentId: string;
  description: string;
  toolsRequired: string[];
  /** Simulated outcome for the task. */
  outcome: 'success' | 'failure' | 'revision';
  /** Trust events generated by this task. */
  trustEvents: TrustEvent[];
  /** Memory entries generated by this task. */
  memoryEntries: MemoryEntry[];
}

/**
 * A multi-agent coordination scenario.
 */
export interface CoordinationScenario {
  name: string;
  description: string;
  agents: AgentConfig[];
  tools: Array<{ registration: ToolRegistration; handler: ToolHandler }>;
  capabilities: OfferedCapability[];
  tasks: ScenarioTask[];
}

/**
 * Outcome of a single task execution within a scenario.
 */
export interface TaskOutcome {
  task: ScenarioTask;
  toolInvocations: number;
  toolSuccesses: number;
  toolFailures: number;
  capabilitiesGranted: number;
  capabilitiesDenied: number;
  memoriesStored: number;
}

/**
 * Full results of running a coordination scenario.
 */
export interface ScenarioResult {
  scenario: string;
  agentsRegistered: number;
  taskOutcomes: TaskOutcome[];
  finalTrustLevels: Map<string, TrustLevel>;
  auditEventCount: number;
  totalMemories: number;
  duration: number;
}

/**
 * Services required to run a scenario.
 */
export interface ScenarioServices {
  runtime: AgentRuntime;
  trust: TrustEngine;
  gateway: ACIGateway;
  capabilities: CapabilityNegotiation;
  memory: MemoryService;
}

/**
 * Run a multi-agent coordination scenario against real service instances.
 */
export async function runScenario(
  scenario: CoordinationScenario,
  services: ScenarioServices,
): Promise<ScenarioResult> {
  const start = Date.now();

  // 1. Register agents in runtime and trust engine
  for (const agentConfig of scenario.agents) {
    await services.runtime.register(agentConfig);
    services.trust.registerAgent(agentConfig.id, agentConfig.trust_level);
  }

  // 2. Register tools in ACI gateway
  for (const { registration, handler } of scenario.tools) {
    services.gateway.registerTool(registration, handler);
  }

  // 3. Register offered capabilities
  for (const cap of scenario.capabilities) {
    services.capabilities.registerOfferedCapability(cap);
  }

  // 4. Execute tasks
  const taskOutcomes: TaskOutcome[] = [];

  for (const task of scenario.tasks) {
    const outcome = await executeTask(task, services);
    taskOutcomes.push(outcome);
  }

  // 5. Collect final trust levels
  const finalTrustLevels = new Map<string, TrustLevel>();
  for (const agentConfig of scenario.agents) {
    const level = services.trust.getTrustLevel(agentConfig.id);
    if (level !== undefined) {
      finalTrustLevels.set(agentConfig.id, level);
    }
  }

  // 6. Count audit events (if available via gateway listing)
  const auditEventCount = 0; // Audit events are internal to the logger

  // 7. Count total memories
  let totalMemories = 0;
  for (const to of taskOutcomes) {
    totalMemories += to.memoriesStored;
  }

  return {
    scenario: scenario.name,
    agentsRegistered: scenario.agents.length,
    taskOutcomes,
    finalTrustLevels,
    auditEventCount,
    totalMemories,
    duration: Date.now() - start,
  };
}

/**
 * Execute a single task within a scenario.
 */
async function executeTask(
  task: ScenarioTask,
  services: ScenarioServices,
): Promise<TaskOutcome> {
  let toolInvocations = 0;
  let toolSuccesses = 0;
  let toolFailures = 0;

  // Transition agent to active
  const agent = services.runtime.getAgent(task.agentId);
  if (agent && agent.state === 'created') {
    await services.runtime.transition(task.agentId, 'initializing');
    await services.runtime.transition(task.agentId, 'ready');
    await services.runtime.transition(task.agentId, 'active');
  } else if (agent && agent.state === 'ready') {
    await services.runtime.transition(task.agentId, 'active');
  }

  // Negotiate capabilities
  const negotiationResult = services.capabilities.negotiate({
    agentId: task.agentId,
    requested: task.toolsRequired.map((op) => ({
      operation: op,
      minVersion: { major: 1, minor: 0, patch: 0 },
    })),
  });

  const capabilitiesGranted = negotiationResult.granted.length;
  const capabilitiesDenied = negotiationResult.denied.length;

  // Invoke tools
  for (const toolName of task.toolsRequired) {
    if (services.gateway.hasTool(toolName)) {
      const result = await services.gateway.invoke({
        toolName,
        input: { task: task.description },
        agentId: task.agentId,
        requestId: `req_${task.agentId}_${toolName}_${Date.now()}`,
      });
      toolInvocations++;
      if (result.success) {
        toolSuccesses++;
      } else {
        toolFailures++;
      }
    }
  }

  // Record trust events
  for (const event of task.trustEvents) {
    services.trust.recordEvent(event);
  }

  // Store memories
  let memoriesStored = 0;
  for (const entry of task.memoryEntries) {
    await services.memory.store(entry);
    memoriesStored++;
  }

  // Calibrate trust after task
  services.trust.calibrate(task.agentId);

  return {
    task,
    toolInvocations,
    toolSuccesses,
    toolFailures,
    capabilitiesGranted,
    capabilitiesDenied,
    memoriesStored,
  };
}

